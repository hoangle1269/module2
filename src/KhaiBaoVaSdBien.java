public class KhaiBaoVaSdBien {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";
        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
        System.out.println("i + f = " + (i + f));
        System.out.println("i + d = " + (i + d));
//        System.out.println("i + b = " + (i + b));
        System.out.println("i + c = " + (i + c));
        System.out.println("i + s = " + (i + s));
        System.out.println("f + d = " + (f + d));
//        System.out.println("f + b = " + (f + b));
        System.out.println("f + c = " + (f + c));
        System.out.println("f + s = " + (f + s));
//        System.out.println("d + b = " + (d + b));
        System.out.println("d + c = " + (d + c));
        System.out.println("d + s = " + (d + s));

    }
}
